def main():
    with open(DATA_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Build lookup
    elem_index = {}
    for fv in data["frameworkViews"]:
        for elem in fv.get("elements", []):
            elem_index[(fv["eventId"], elem.get("elementId"))] = elem

    rewritten = 0
    not_found = []

    for eid, elem_id, new_label in REWRITES:
        key = (eid, elem_id)
        if key not in elem_index:
            not_found.append(key)
            continue
        elem = elem_index[key]
        old_label = elem["label"]
        elem["label"] = new_label
        rewritten += 1

    # Save
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # Verify: check remaining F-E overlaps
    from difflib import SequenceMatcher
    high_overlap = 0
    for fv in data["frameworkViews"]:
        f_labels = [el.get("label","") for el in fv.get("elements",[]) if el["layer"]=="F"]
        e_labels = [el.get("label","") for el in fv.get("elements",[]) if el["layer"]=="E"]
        title = fv["title"]
        for fl in f_labels:
            for el in e_labels:
                if SequenceMatcher(None, fl, el).ratio() > 0.4:
                    high_overlap += 1
            if SequenceMatcher(None, fl, title).ratio() > 0.5:
                high_overlap += 1

    print(f"=== v3.7 F-Eトリガー修正 ===")
    print(f"書き換え: {rewritten}/{len(REWRITES)}件")
    if not_found:
        print(f"未発見: {len(not_found)}件")
        for k in not_found:
            print(f"  {k}")
    print(f"\nF-E高類似度残存(>0.4): {high_overlap}件")
    print(f"\n>>> {DATA_FILE} を上書き保存しました。")


if __name__ == "__main__":
    main()
