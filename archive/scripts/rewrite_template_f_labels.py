def main():
    with open(DATA_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    updated = 0
    not_found = []

    for fv in data["frameworkViews"]:
        eid = fv["eventId"]
        if eid not in ALL_LABELS:
            continue

        new_label = ALL_LABELS[eid]
        found = False

        for elem in fv.get("elements", []):
            if elem.get("layer") != "F":
                continue
            old_label = elem.get("label", "")

            # マッチ条件: テンプレートパターンを含む、またはCNF01/02パターン
            is_template = any(t in old_label for t in TEMPLATES)
            is_cnf01 = "をめぐる利害対立" in old_label
            is_cnf02 = "軍事衝突が臨界に達し" in old_label

            if is_template or is_cnf01 or is_cnf02:
                elem["label"] = new_label
                updated += 1
                found = True
                break  # 1イベント1F要素のみ更新

        if not found:
            not_found.append(eid)

    # 保存
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"=== v3.5 テンプレートFラベル個別化 ===")
    print(f"更新対象: {len(ALL_LABELS)}件")
    print(f"更新成功: {updated}件")
    print(f"未発見: {len(not_found)}件")
    if not_found:
        print(f"  未発見ID: {not_found}")
    print(f">>> {DATA_FILE} を上書き保存しました。")


if __name__ == "__main__":
    main()
