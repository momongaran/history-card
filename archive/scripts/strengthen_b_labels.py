def main():
    with open(DATA_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Build element lookup: (eventId, elementId) -> element dict
    elem_lookup = {}
    for fv in data["frameworkViews"]:
        for elem in fv.get("elements", []):
            elem_lookup[(fv["eventId"], elem.get("elementId"))] = elem

    updated = 0
    not_found = []
    unchanged = []

    for eid, elem_id, new_label in REWRITES:
        key = (eid, elem_id)
        if key not in elem_lookup:
            not_found.append((eid, elem_id))
            continue

        elem = elem_lookup[key]
        old_label = elem.get("label", "")
        if old_label == new_label:
            unchanged.append(eid)
            continue

        elem["label"] = new_label
        updated += 1

    # Save
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"=== v3.5 B記述強化 ===")
    print(f"書き換え対象: {len(REWRITES)}件")
    print(f"更新成功: {updated}件")
    print(f"変更なし: {len(unchanged)}件")
    print(f"未発見: {len(not_found)}件")
    if not_found:
        for eid, elem_id in not_found:
            print(f"  {eid} / {elem_id}")

    # サンプル出力
    print(f"\n--- 書き換えサンプル (最初の5件) ---")
    for i, (eid, elem_id, new_label) in enumerate(REWRITES[:5]):
        print(f"  [{eid}] {new_label[:60]}...")

    print(f"\n>>> {DATA_FILE} を上書き保存しました。")


if __name__ == "__main__":
    main()
