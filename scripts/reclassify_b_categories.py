def main():
    with open(DATA_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    elem_index = {}
    for fv in data["frameworkViews"]:
        for elem in fv.get("elements", []):
            elem_index[(fv["eventId"], elem.get("elementId"))] = elem

    reassigned = 0
    not_found = []

    for eid, elid, new_cat, new_sc in REASSIGN:
        key = (eid, elid)
        if key not in elem_index:
            not_found.append(key)
            continue
        elem = elem_index[key]
        old_sc = elem.get("subCategory", "?")
        elem["category"] = new_cat
        elem["subCategory"] = new_sc
        reassigned += 1

    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # Stats
    from collections import Counter
    dup_events = 0
    b_sc_counts = Counter()
    for fv in data["frameworkViews"]:
        b_scs = [el.get("subCategory", "") for el in fv.get("elements", []) if el["layer"] == "B"]
        b_sc_counts.update(b_scs)
        sc_counter = Counter(b_scs)
        if any(c >= 2 for c in sc_counter.values()):
            dup_events += 1

    print(f"=== v3.7d B層サブカテゴリ再分類 ===")
    print(f"再分類: {reassigned}/{len(REASSIGN)}件")
    if not_found:
        print(f"未発見: {len(not_found)}件")
        for k in not_found:
            print(f"  {k}")

    # B-INS-04 remaining
    ins04_count = b_sc_counts.get("B-INS-04", 0)
    print(f"\nB-INS-04残存: {ins04_count}件 (旧166件)")
    print(f"B重複イベント残存: {dup_events}件 (旧101件)")

    print(f"\nF/EがBに混入している可能性: {len(FE_IN_B)}件（今回は報告のみ）")
    for eid, elid, note in FE_IN_B[:5]:
        print(f"  {eid}/{elid}: {note}")
    if len(FE_IN_B) > 5:
        print(f"  ... 他{len(FE_IN_B)-5}件")

    print(f"\n>>> {DATA_FILE} を上書き保存しました。")


if __name__ == "__main__":
    main()
