def abstract_title(title, e_category):
    """eventタイトルを一段抽象化する"""
    cat_rules = ABSTRACTION_RULES.get(e_category)
    if not cat_rules:
        return title  # 未知カテゴリはそのまま

    # 1. キーワードベースの精緻化を最初にチェック
    for keyword, cat_map in KEYWORD_QUALIFIERS.items():
        if keyword in title and e_category in cat_map:
            # キーワードが見つかったが、より具体的なパターンマッチも試す
            pass

    # 2. パターンマッチで抽象化
    for pattern, abstract_expr in cat_rules['patterns']:
        if re.search(pattern, title):
            # パターンにマッチした場合、タイトルの前半部分から補足情報を抽出
            prefix = re.sub(pattern, '', title).strip()
            prefix = remove_proper_nouns(prefix)

            # 前半に意味のある修飾語があれば追加
            qualifier = extract_qualifier(prefix, e_category)
            if qualifier:
                return qualifier + 'による' + abstract_expr if len(qualifier + 'による' + abstract_expr) <= 35 else abstract_expr
            return abstract_expr

    # 3. キーワードベースのフォールバック
    for keyword, cat_map in KEYWORD_QUALIFIERS.items():
        if keyword in title and e_category in cat_map:
            return cat_map[e_category]

    # 4. デフォルト
    return cat_rules['default']


def extract_qualifier(prefix, e_category):
    """除去後の前半部分から意味のある修飾語を抽出する"""
    if not prefix or len(prefix) < 2:
        return None

    # 余計な助詞を除去
    prefix = re.sub(r'^[がのをにへでと、]+', '', prefix)
    prefix = re.sub(r'[がのをにへでと、]+$', '', prefix)

    if not prefix or len(prefix) < 2:
        return None

    # 意味のある修飾語のパターン
    qualifiers = {
        'E-WAR': ['対立', '反乱', '蜂起', '内紛', '抗争', '武力', '軍事'],
        'E-POW': ['権力', '政権', '実権', '統治', '王位', '朝廷'],
        'E-SYS': ['制度', '法制', '行政', '官制', '税制', '改革'],
        'E-REG': ['体制', '幕府', '政権', '統治', '改革'],
        'E-CUL': ['文化', '宗教', '思想', '学問', '芸術'],
        'E-ADP': ['外交', '通商', '対外', '開国', '鎖国'],
        'E-ECO': ['経済', '財政', '商業', '農業', '貨幣'],
        'E-COL': ['崩壊', '危機', '混乱', '動揺', '衰退'],
    }

    cat_keywords = qualifiers.get(e_category, [])
    for kw in cat_keywords:
        if kw in prefix:
            return prefix

    # 修飾語が短すぎる or 無関係ならNone
    if len(prefix) > 15:
        return None

    return None


