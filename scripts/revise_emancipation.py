def remove_proper_nouns(text):
    """固有名詞を除去する（長い名前から順に）"""
    result = text
    # 長い名前から順に除去（部分一致の問題を避ける）
    all_names = sorted(PERSON_NAMES + PLACE_NAMES, key=len, reverse=True)
    for name in all_names:
        result = result.replace(name, '')
    # 残った助詞の重複を整理
    result = re.sub(r'[がのをにへでと]{2,}', lambda m: m.group(0)[0], result)
    result = re.sub(r'^[がのをにへでと]+', '', result)
    return result.strip()


