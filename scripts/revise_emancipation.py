def process_all(data):
    """全FWのE要素を処理する"""
    items = {}

    for fv in data['frameworkViews']:
        fw_id = fv['frameworkViewId']
        title = fv['title']

        for el in fv['elements']:
            if el.get('layer') != 'E':
                continue

            element_id = el['elementId']
            original_label = el['label']
            e_category = el.get('category', '')
            e_subcategory = el.get('subCategory', '')

            # 特殊ケース辞書を優先
            if fw_id in SPECIAL_CASES:
                revised_label = SPECIAL_CASES[fw_id]
            else:
                revised_label = abstract_title(title, e_category)

            items[fw_id] = {
                'elementId': element_id,
                'originalLabel': original_label,
                'revisedLabel': revised_label,
                'categoryRevised': e_category,
                'subCategoryRevised': e_subcategory,
            }

    return items


