def main():
    with open(INPUT, encoding="utf-8") as f:
        data = json.load(f)

    old_node_count = len(data["nodes"])
    old_edge_count = len(data["edges"])

    # Insert P0 nodes at the beginning
    data["nodes"] = P0_NODES + data["nodes"]

    # Append Q-W nodes at the end
    data["nodes"].extend(QW_NODES)

    # Add new edges (note: existing E094 will be kept; new E094 is also added)
    data["edges"].extend(NEW_EDGES)

    # Update meta
    new_node_count = len(data["nodes"])
    new_edge_count = len(data["edges"])

    data["meta"]["version"] = "2.0"
    data["meta"]["description"] = (
        "EnergyAbs有向グラフ v2: 継体天皇(5c)→オイルショック(1975) "
        "約1500年の因果フロー"
    )
    data["meta"]["nodeCount"] = new_node_count
    data["meta"]["edgeCount"] = new_edge_count
    data["meta"]["updated"] = "2026-03-20"

    # Validate: check all edge endpoints exist
    node_ids = {n["id"] for n in data["nodes"]}
    missing = []
    for e in data["edges"]:
        if e["from"] not in node_ids:
            missing.append(f"Edge {e['id']}: from={e['from']} not found")
        if e["to"] not in node_ids:
            missing.append(f"Edge {e['id']}: to={e['to']} not found")

    if missing:
        print("WARNING: dangling edge references:")
        for m in missing:
            print(f"  {m}")

    # Check for duplicate edge IDs
    edge_ids = [e["id"] for e in data["edges"]]
    seen = set()
    dupes = []
    for eid in edge_ids:
        if eid in seen:
            dupes.append(eid)
        seen.add(eid)
    if dupes:
        print(f"NOTE: duplicate edge IDs: {dupes}")

    # Write output
    with open(OUTPUT, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # Collect cluster info
    clusters = {}
    for n in data["nodes"]:
        c = n["cluster"]
        clusters[c] = clusters.get(c, 0) + 1

    # Summary
    print(f"Input:  {INPUT.name}  ({old_node_count} nodes, {old_edge_count} edges)")
    print(f"Output: {OUTPUT.name} ({new_node_count} nodes, {new_edge_count} edges)")
    print(f"Added:  {new_node_count - old_node_count} nodes, {new_edge_count - old_edge_count} edges")
    print()
    print("Clusters:")
    for c, cnt in sorted(clusters.items()):
        print(f"  {c}: {cnt} nodes")


if __name__ == "__main__":
    main()
